package cn.trace.pojo;

public class Dealer {
	Integer id;//id
	String dealername;//经销商姓名
	String dealeraddress;//经销商地址
	String phone;//电话
	Integer dealernum;//经销商编号
	Integer producternum;//合作企业编号
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDealername() {
		return dealername;
	}
	public void setDealername(String dealername) {
		this.dealername = dealername;
	}
	public String getDealeraddress() {
		return dealeraddress;
	}
	public void setDealeraddress(String dealeraddress) {
		this.dealeraddress = dealeraddress;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public Integer getDealernum() {
		return dealernum;
	}
	public void setDealernum(Integer dealernum) {
		this.dealernum = dealernum;
	}
	public Integer getProducternum() {
		return producternum;
	}
	public void setProducternum(Integer producternum) {
		this.producternum = producternum;
	}
	
	
	
}
