package cn.trace.pojo;

public class Product {


	Integer id;//id
	Integer productnum;//产品编号
	String productname;//产品名称
	String brand;//产品品牌
	Integer producternum;//生产企业编号
	String productpicture;//产品图片路径
	Integer brandnum;//产品品牌编号
	String Productdes;//产品描述
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getProductnum() {
		return productnum;
	}
	public void setProductnum(Integer productnum) {
		this.productnum = productnum;
	}
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public Integer getProducternum() {
		return producternum;
	}
	public void setProducternum(Integer producternum) {
		this.producternum = producternum;
	}
	public String getProductpicture() {
		return productpicture;
	}
	public void setProductpicture(String productpicture) {
		this.productpicture = productpicture;
	}
	public Integer getBrandnum() {
		return brandnum;
	}
	public void setBrandnum(Integer brandnum) {
		this.brandnum = brandnum;
	}
	public String getProductdes() {
		return Productdes;
	}
	public void setProductdes(String productdes) {
		Productdes = productdes;
	}
	
	
	

}
