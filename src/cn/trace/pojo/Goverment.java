package cn.trace.pojo;

public class Goverment {
	Integer id;//Lid
	String govuser;//政府账号
	String govpassword;//密码
	String username;//用户姓名
	String linkphone;//联系电话
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getGovuser() {
		return govuser;
	}
	public void setGovuser(String govuser) {
		this.govuser = govuser;
	}
	public String getGovpassword() {
		return govpassword;
	}
	public void setGovpassword(String govpassword) {
		this.govpassword = govpassword;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getLinkphone() {
		return linkphone;
	}
	public void setLinkphone(String linkphone) {
		this.linkphone = linkphone;
	}

}
